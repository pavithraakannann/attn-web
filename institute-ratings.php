<script>
    // Add event listeners to all rating stars
    document.querySelectorAll('.rating-star').forEach(item => {
        item.addEventListener('click', event => {
            const rating = parseInt(item.getAttribute('data-rating'));

            // Remove 'active' class from all rating stars
            document.querySelectorAll('.rating-star').forEach(item => {
                item.classList.remove('active');
            });

            // Add 'active' class to clicked rating star and previous stars
            for (let i = 1; i <= rating; i++) {
                document.querySelector(`.rating-star[data-rating="${i}"]`).classList.add('active');
            }


        });
    });
</script>



