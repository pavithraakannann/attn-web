    (function($) {
        $(function() {
            console.log("ready!");

            var liveChat = $(".live-chat");
            var liveChatTrigger = $(".live-chat .trigger");
            var liveChatClose = $(".live-chat .start .close");
            liveChatTrigger.click(function() {
                if (liveChat.hasClass("open")) {
                    liveChat.removeClass("open");
                } else {
                    liveChat.addClass("open");
                }
            });
            liveChatClose.click(function() {
                liveChat.removeClass("open");
            });

        });
    })(jQuery);

    function sendMessage(tab) {
        var inputField = document.getElementById(tab + '-input');
        var messagesContainer = document.getElementById(tab + '-messages');
        var messageText = inputField.value;

        if (messageText.trim() !== '') {
            var messageElement = document.createElement('div');
            messageElement.textContent = 'You: ' + messageText;
            messagesContainer.appendChild(messageElement);

            // Simulate a response from the server
            setTimeout(function() {
                var responseElement = document.createElement('div');
                responseElement.textContent = 'Agent: This is a response to "' + messageText + '"';
                messagesContainer.appendChild(responseElement);

                // Scroll to the bottom of the chat
                messagesContainer.scrollTop = messagesContainer.scrollHeight;
            }, 1000);

            // Clear the input field
            inputField.value = '';
        }
    }

    function selectQuestion(tab, question) {
        var inputField = document.getElementById(tab + '-input');
        inputField.value = question;
    }
</script>






